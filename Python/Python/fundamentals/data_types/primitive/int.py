# int: whole numbers, positive or negative, no decimal point

x = 10
y = -5
big = 1_000_000   # underscores improve readability

print(x + y)      # 5
print(type(x))    # <class 'int'>
print(big)        # 1000000

# Integer operations
print(10 // 3)    # floor division → 3
print(10 % 3)     # modulo → 1
print(2 ** 8)     # power → 256
