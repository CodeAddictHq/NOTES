# if/else: execute code based on a condition

age = 20

if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# elif for multiple branches
score = 75

if score >= 90:
    print("Grade: A")
elif score >= 75:
    print("Grade: B")
elif score >= 60:
    print("Grade: C")
else:
    print("Grade: F")
