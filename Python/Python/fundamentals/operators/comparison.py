# Comparison operators: compare values and return bool

a = 5
b = 10

print(a == b)   # equal → False
print(a != b)   # not equal → True
print(a < b)    # less than → True
print(a > b)    # greater than → False
print(a <= 5)   # less than or equal → True
print(b >= 10)  # greater than or equal → True

# Chaining comparisons
x = 7
print(1 < x < 10)  # True
